#ifndef _NOISE_GENERATOR_H_
#define _NOISE_GENERATOR_H_ 1

#include "reader.h"
#include "scheduler.h"
#include "trace_entry.h"

namespace dynamorio {
namespace drmemtrace {

/**
 * Generates synthetic #dynamorio::drmemtrace::memref_t trace entries in a single thread
 * and presents them via an iterator interface to the scheduler.
 */
class noise_generator_t : public reader_t {
public:
    noise_generator_t();

    noise_generator_t(addr_t pid, addr_t tid, uint64_t num_records_to_generate);

    virtual ~noise_generator_t();

    bool
    init() override;

    std::string
    get_stream_name() const override;

protected:
    // Wraps the noise records generated by generate_trace_entry() between
    // TRACE_TYPE_THREAD, TRACE_TYPE_PID and TRACE_TYPE_THREAD_EXIT.
    virtual trace_entry_t *
    read_next_entry() override;

    // Has the main logic to generate noise records.
    virtual trace_entry_t
    generate_trace_entry();

    // This counter does not count TRACE_TYPE_THREAD or TRACE_TYPE_PID.
    // The idea is that when we want to generate at least 1 record, tid and pid have to be
    // there as well, otherwise the scheduler will report an error.
    uint64_t num_records_to_generate_ = 0;
    addr_t pid_ = 0;
    addr_t tid_ = 0;

private:
    trace_entry_t entry_ = {};
    bool pid_generated_ = false;
    bool tid_generated_ = false;
    bool marker_timestamp_generated_ = false;
};

/**
 * Contains metadata information to drive the noise generation.
 */
struct noise_generator_info_t {
    // TODO i#7216: temporary default values.
    uint64_t num_processes = 1;
    uint64_t num_threads_per_process = 1;
    uint64_t num_records_to_generate = 1000;
};

/**
 * Factory to create noise_generator_t.
 */
template <typename RecordType, typename ReaderType> class noise_generator_factory_t {
public:
    typedef scheduler_tmpl_t<RecordType, ReaderType> sched_type_t;

    void
    init();

    bool
    is_enabled();

    std::string
    get_error_string();

    void
    add_noise_generator_to_workloads(
        std::vector<typename sched_type_t::input_workload_t> &workloads);

protected:
    std::unique_ptr<ReaderType>
    create_noise_generator(addr_t pid, addr_t tid, uint64_t num_records);

    std::unique_ptr<ReaderType>
    create_noise_generator_end();

    noise_generator_info_t info_;
    bool enabled_ = false;
    std::string error_string_;
};

} // namespace drmemtrace
} // namespace dynamorio

#endif /* _NOISE_GENERATOR_H_ */
